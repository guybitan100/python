user = "Tuna McFish"
# Print the first char
print(user[0])
# Print the last char
print(user[-1])
# Print 3 chars from the end
print(user[-3])
# Print from char 2 to char 7
print(user[2:7])
# Print from beginning char to char 7
print(user[:7])
# Print from char 2 to end
print(user[2:])
# Print all string
print(user[:])
# Print the length of user
print(len(user))
